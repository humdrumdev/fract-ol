#include <unistd.h>

int	ft_putendl_fd(char *s, int fd)
{
	int	len;

	len = 0;
	if (fd < 0 || !s)
		return (-1);
	while (*s)
	{
		len += write(fd, s, 1);
		s++;
	}
	return (write(fd, "\n", 1) + len);
}
